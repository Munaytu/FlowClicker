const contractAddress = "0xa2989aFce34F5D126858a39BA9409aDc115906A8";

export async function POST(req: Request) {
  try {
    const { player, clicks } = await req.json();

    if (!player || !clicks) {
      return NextResponse.json({ error: "Player address and clicks are required" }, { status: 400 });
    }

    const privateKey = process.env.OWNER_PRIVATE_KEY;
    if (!privateKey) {
      console.error("OWNER_PRIVATE_KEY is not set in .env.local");
      return NextResponse.json({ error: "Server configuration error" }, { status: 500 });
    }

    const account = privateKeyToAccount(privateKey as `0x${string}`);

    const publicClient = createPublicClient({
      chain: sonicMainnet,
      transport: http(),
    });

    let nonce;
    try {
      nonce = await publicClient.readContract({
        address: contractAddress,
        abi: contractAbi,
        functionName: 'nonces',
        args: [player],
      });
    } catch (e) {
        console.error("Failed to read nonce", e);
        throw e;
    }

    const walletClient = createWalletClient({
        account,
        chain: sonicMainnet,
        transport: http(),
    });

    // EIP712 Domain
    const domain = {
      name: "FlowClicker",
      version: "1",
      chainId: sonicMainnet.id,
      verifyingContract: contractAddress,
    } as const;

    // EIP712 Types
    const types = {
      Claim: [
        { name: "player", type: "address" },
        { name: "clicks", type: "uint256" },
        { name: "nonce", type: "uint256" },
      ],
    } as const;

    // Message to sign
    const message = {
      player: player,
      clicks: BigInt(clicks),
      nonce: BigInt(nonce as number),
    } as const;

    // Sign the typed data
    const signature = await walletClient.signTypedData({
      account,
      domain,
      types,
      primaryType: "Claim",
      message,
    });

    return NextResponse.json({ signature, nonce: nonce.toString() });

  } catch (error) {
    console.error("Error in get-claim-signature API:", error);
    const errorMessage = error instanceof Error ? error.message : "An unknown error occurred";
    return NextResponse.json({ error: "Failed to generate signature", details: errorMessage }, { status: 500 });
  }
}
